package HomeWork_1;

public class Sum_dimensional_array {
    public static void main(String[] args) {
        int[] arrInt = {5, 10, 15, 20, 25};

        int sum = 0;
        for (int j : arrInt) {
            sum += j;
        }

        System.out.println("Сумма элементов массива: " + sum);
    }
}



